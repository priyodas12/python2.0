numbers = list(range(1, 9))
#[1, 2, 3, 4, 5, 6, 7, 8, 9]

print(numbers)

# [start_index : stop_index(exlusive)]
print(numbers[1:5])

# from start_index to end
print(numbers[4:])

# from begining to end_index(exlusive)
print(numbers[:5])

# start_index(2) : end_index(exlusive>9) : interval
print(numbers[1:8:2])
